# What happens when a comparison is encountered
print("1 == 2, returns: " + str(1 == 2))#1 equals 2
print("1 != 2, returns: " + str(1 != 2))# not equal to 2
print("1 > 2, returns: " + str(1 > 2))#1 is not greater than 2
print("1 < 2, returns: " + str(1 < 2))#2 is greater than 1
print("1 >= 2, returns: " + str(1 >= 2))# 1 is not greater or equal to 2
print("1 <= 1, returns: " + str(1 <= 1))# 1 is greater or equal than 1