# We can use the "else" in a "while" loop.

count = 0
while (count < 5):
    print(count, " is less than 5")
    count = count + 1
else:
    #yes we can use an 'else' with 'while' loop not only 'if'.
    #in fact many checks with : support 'else' behind them!
    print(count, " is not less than 5")



